#include "Common.h"

// -----------------------------------------------------------------------------
Point::Point(void) {
	m_x = 0;
	m_y = 0;
}

// -----------------------------------------------------------------------------
Point::Point(int16_t x, int16_t y) {
	m_x = x;
	m_y = y;
}

// -----------------------------------------------------------------------------
Point::~Point(void) {

}

// -----------------------------------------------------------------------------
int16_t Point::x() const {
	return m_x;
}

// -----------------------------------------------------------------------------
int16_t Point::y() const {
	return m_y;
}

// -----------------------------------------------------------------------------
void Point::setX(int16_t x) {
	m_x = x;
}

// -----------------------------------------------------------------------------
void Point::setY(int16_t y) {
	m_y = y;
}

// -----------------------------------------------------------------------------
bool Point::isNull() const {
	return ((m_x == 0) && (m_y == 0));
}
