#include "Common.h"

// -----------------------------------------------------------------------------
Colour::Colour() {

}

// -----------------------------------------------------------------------------
Colour::Colour(int32_t colour) {
	m_value = colour;
}

// -----------------------------------------------------------------------------
Colour::~Colour() {

}

// -----------------------------------------------------------------------------
int32_t Colour::value() const {
	return m_value;
}

// -----------------------------------------------------------------------------
uint16_t Colour::to16bit() const {
	return (uint16_t)(((m_value & 0x00F80000) >> 8) | ((m_value & 0x0000FC00) >> 5) | ((m_value & 0x000000F8) >> 3));
}

// -----------------------------------------------------------------------------
Colour Colour::fromArgb(uint8_t a, uint8_t r, uint8_t g, uint8_t b) {
	return Colour((a << 24) | (r << 16) | (g << 8) | b);
}
